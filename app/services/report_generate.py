def _rrect(c, x, y, w, h, r=8, fill=None, stroke_color=None, sw=0.5):
    p = c.beginPath()
    p.moveTo(x + r, y)
    p.lineTo(x + w - r, y)
    p.arcTo(x + w - 2*r, y,           x + w,    y + 2*r,    0, -90)
    p.lineTo(x + w,      y + h - r)
    p.arcTo(x + w - 2*r, y + h - 2*r, x + w,    y + h,      0,  90)
    p.lineTo(x + r,      y + h)
    p.arcTo(x,           y + h - 2*r, x + 2*r,  y + h,     90,  90)
    p.lineTo(x,          y + r)
    p.arcTo(x,           y,           x + 2*r,  y + 2*r,  180,  90)
    p.close()
    if fill:
        c.setFillColor(fill)
    if stroke_color:
        c.setStrokeColor(stroke_color)
        c.setLineWidth(sw)
    c.drawPath(p, fill=int(bool(fill)), stroke=int(bool(stroke_color)))


def _draw_shared_header(c, W, H, logo_path, subtitle="STUDENT PLATFORM  |  ANALYTICS"):
    c.setFillColor(WHITE)
    c.rect(0, H - 70, W, 70, fill=1, stroke=0)
    c.setStrokeColor(HexColor("#E0E0E0"))
    c.setLineWidth(1)
    c.line(0, H - 70, W, H - 70)

    logo_size = 44
    logo_x    = 18
    logo_y    = H - 57
    drawn     = False

    if logo_path and os.path.exists(logo_path):
        try:
            c.drawImage(logo_path, logo_x, logo_y,
                        width=logo_size, height=logo_size,
                        preserveAspectRatio=True, mask="auto")
            drawn = True
        except Exception:
            pass

    if not drawn:
        c.setFillColor(DARK)
        c.circle(logo_x + logo_size/2, logo_y + logo_size/2, logo_size/2, fill=1, stroke=0)
        c.setFillColor(WHITE)
        c.setFont("Helvetica-Bold", 9)
        c.drawCentredString(logo_x + logo_size/2, logo_y + logo_size/2 - 4, "IF")

    tx = logo_x + logo_size + 10
    c.setFillColor(DARK)
    c.setFont("Helvetica-Bold", 18)
    c.drawString(tx, H - 30, "INTELLECTAFLOW")
    c.setFillColor(TEXT_MID)
    c.setFont("Helvetica", 9)
    c.drawString(tx, H - 45, subtitle)


def _draw_shared_footer(c, W):
    c.setFillColor(HexColor("#E0E0E0"))
    c.rect(0, 0, W, 26, fill=1, stroke=0)
    c.setFont("Helvetica", 8)
    c.setFillColor(TEXT_LIGHT)
    c.drawCentredString(W / 2, 8,
        "Generated by IntellectaFlow  \u2022  Student Adaptive Testing Platform")


def _draw_student_info_card(c, margin, top_y, W, student: dict) -> float:
    """Draw student info card. Returns y below it."""
    card_w = W - 2 * margin
    card_h = 50
    _rrect(c, margin, top_y - card_h, card_w, card_h, fill=WHITE, stroke_color=BORDER2)
    c.setFont("Helvetica-Bold", 11)
    c.setFillColor(TEXT_DARK)
    c.drawString(margin + 14, top_y - 19, student.get("name", "N/A"))
    c.setFont("Helvetica", 9)
    c.setFillColor(TEXT_MID)
    c.drawString(
        margin + 14, top_y - 36,
        f"USN: {student.get('usn','N/A')}   |   "
        f"Branch: {student.get('branch','N/A')}   |   "
        f"Section: {student.get('section','N/A')}"
    )
    return top_y - card_h - 12


