def _pie(c, cx, cy, r, slices):
    start = 90
    for s in slices:
        angle = s["pct"] * 360
        c.setFillColor(s["color"])
        c.setStrokeColor(WHITE)
        c.setLineWidth(1.5)
        c.wedge(cx - r, cy - r, cx + r, cy + r, start, angle, fill=1, stroke=1)
        if s["pct"] >= 0.06:
            mid_angle = math.radians(start + angle / 2)
            label_r   = r * 0.62
            lx = cx + label_r * math.cos(mid_angle)
            ly = cy + label_r * math.sin(mid_angle)
            c.setFont("Helvetica-Bold", 7)
            c.setFillColor(WHITE)
            c.drawCentredString(lx, ly - 3, f"{round(s['pct'] * 100)}%")
        start += angle


def _line_chart(c, x0, y0, width, height, data_points, avg_points=None):
    if not data_points:
        return
    PAD_L, PAD_R, PAD_T, PAD_B = 30, 10, 14, 22
    iW = width  - PAD_L - PAD_R
    iH = height - PAD_T - PAD_B

    scores   = [d["score"] for d in data_points]
    all_vals = scores[:]
    if avg_points:
        all_vals += avg_points

    raw_max = max(all_vals) if all_vals else 100
    raw_min = min(all_vals) if all_vals else 0
    padding = max((raw_max - raw_min) * 0.15, 5)
    v_max   = min(raw_max + padding, 105)
    v_min   = max(raw_min - padding, 0)
    v_range = v_max - v_min or 1
    n       = len(data_points)

    def px(i):
        if n == 1:
            return x0 + PAD_L + iW / 2
        return x0 + PAD_L + i * (iW / (n - 1))

    def py(v):
        return y0 + PAD_B + ((v - v_min) / v_range) * iH

    _rrect(c, x0, y0, width, height, 8, fill=WHITE)

    steps     = 4
    step_size = v_range / steps
    c.setFont("Helvetica", 7)
    for i in range(steps + 1):
        gv = v_min + i * step_size
        gy = py(gv)
        c.setStrokeColor(HexColor("#E8E8F0"))
        c.setLineWidth(0.5)
        c.setDash(3, 3)
        c.line(x0 + PAD_L, gy, x0 + PAD_L + iW, gy)
        c.setDash()
        c.setFillColor(TEXT_LIGHT)
        c.drawRightString(x0 + PAD_L - 3, gy - 3, str(int(round(gv))))

    step = max(1, math.ceil(n / 5))
    c.setFont("Helvetica", 7)
    c.setFillColor(TEXT_LIGHT)
    for i, d in enumerate(data_points):
        if i % step == 0 or i == n - 1:
            c.drawCentredString(px(i), y0 + 6, d["label"])

    lx = x0 + PAD_L + iW - 5
    ly = y0 + height - 10
    c.setFillColor(YOU_COLOR)
    c.circle(lx - 62, ly, 3, fill=1, stroke=0)
    c.setFont("Helvetica", 7.5)
    c.setFillColor(TEXT_MID)
    c.drawString(lx - 56, ly - 3, "You")
    if avg_points:
        c.setFillColor(AVG_COLOR)
        c.circle(lx - 22, ly, 3, fill=1, stroke=0)
        c.setFillColor(TEXT_MID)
        c.drawString(lx - 16, ly - 3, "Class Avg")

    if avg_points and len(avg_points) == n:
        c.setStrokeColor(AVG_COLOR)
        c.setLineWidth(1.5)
        c.setDash(5, 3)
        path = c.beginPath()
        path.moveTo(px(0), py(avg_points[0]))
        for i in range(1, n):
            path.lineTo(px(i), py(avg_points[i]))
        c.drawPath(path, stroke=1, fill=0)
        c.setDash()
        for i, av in enumerate(avg_points):
            c.setFillColor(AVG_COLOR)
            c.setStrokeColor(WHITE)
            c.setLineWidth(1.2)
            c.circle(px(i), py(av), 2.5, fill=1, stroke=1)

    if n >= 2:
        pts       = [(px(i), py(scores[i])) for i in range(n)]
        fill_path = c.beginPath()
        fill_path.moveTo(pts[0][0], pts[0][1])
        for fx, fy in pts[1:]:
            fill_path.lineTo(fx, fy)
        fill_path.lineTo(pts[-1][0], y0 + PAD_B)
        fill_path.lineTo(pts[0][0],  y0 + PAD_B)
        fill_path.close()
        c.setFillColor(YOU_COLOR)
        c.setFillAlpha(0.08)
        c.drawPath(fill_path, stroke=0, fill=1)
        c.setFillAlpha(1.0)

    c.setStrokeColor(YOU_COLOR)
    c.setLineWidth(2)
    c.setDash()
    line_path = c.beginPath()
    line_path.moveTo(px(0), py(scores[0]))
    for i in range(1, n):
        line_path.lineTo(px(i), py(scores[i]))
    c.drawPath(line_path, stroke=1, fill=0)

    for i, d in enumerate(data_points):
        cx2, cy2 = px(i), py(d["score"])
        c.setFillColor(YOU_COLOR)
        c.setStrokeColor(WHITE)
        c.setLineWidth(1.5)
        c.circle(cx2, cy2, 4, fill=1, stroke=1)


def build_student_report(
    student:   dict,
    stats:     dict,
    subjects:  list,
    attempts:  list,
    logo_path: str = None,
) -> bytes:
    """Original analytics / progress dashboard PDF (unchanged)."""
    W, H   = A4
    margin = 28
    buf    = io.BytesIO()
    c      = rl_canvas.Canvas(buf, pagesize=A4)

    c.setFillColor(BG)
    c.rect(0, 0, W, H, fill=1, stroke=0)

    _draw_shared_header(c, W, H, logo_path)

    c.setFillColor(WHITE)
    c.rect(0, H - 70, W, 70, fill=1, stroke=0)
    c.setStrokeColor(HexColor("#E0E0E0"))
    c.setLineWidth(1)
    c.line(0, H - 70, W, H - 70)

    logo_size = 44
    logo_x    = 18
    logo_y    = H - 57
    logo_drawn = False
    if logo_path and os.path.exists(logo_path):
        try:
            c.drawImage(logo_path, logo_x, logo_y,
                        width=logo_size, height=logo_size,
                        preserveAspectRatio=True, mask="auto")
            logo_drawn = True
        except Exception:
            pass
    if not logo_drawn:
        c.setFillColor(DARK)
        c.circle(logo_x + logo_size/2, logo_y + logo_size/2, logo_size/2, fill=1, stroke=0)
        c.setFillColor(WHITE)
        c.setFont("Helvetica-Bold", 9)
        c.drawCentredString(logo_x + logo_size/2, logo_y + logo_size/2 - 4, "IF")

    text_start_x = logo_x + logo_size + 10
    c.setFillColor(DARK)
    c.setFont("Helvetica-Bold", 18)
    c.drawString(text_start_x, H - 30, "INTELLECTAFLOW")
    c.setFillColor(TEXT_MID)
    c.setFont("Helvetica", 9)
    c.drawString(text_start_x, H - 45, "STUDENT PLATFORM  |  ANALYTICS")

    info_y = H - 130
    _rrect(c, margin, info_y, W - 2*margin, 50, 8, fill=WHITE)
    c.setFont("Helvetica-Bold", 11)
    c.setFillColor(TEXT_DARK)
    c.drawString(margin + 14, info_y + 31, student.get("name", "N/A"))
    c.setFont("Helvetica", 9)
    c.setFillColor(TEXT_MID)
    c.drawString(margin + 14, info_y + 14,
        f"USN: {student.get('usn','N/A')}   |   "
        f"Branch: {student.get('branch','N/A')}   |   "
        f"Section: {student.get('section','N/A')}")

    pt_y = info_y - 28
    c.setFont("Helvetica-Bold", 11)
    c.setFillColor(TEXT_DARK)
    c.drawString(margin, pt_y, "Progress Tracker")

    card_y = pt_y - 72
    card_h = 62
    card_w = (W - 2*margin - 16) / 3
    for i, (label, value) in enumerate([
        ("Total Tests Taken", str(stats.get("tests_taken", 0))),
        ("Average Score",     f"{stats.get('avg_score', 0)}%"),
        ("Best Score",        f"{stats.get('best_score', 0)}%"),
    ]):
        cx = margin + i * (card_w + 8)
        _rrect(c, cx, card_y, card_w, card_h, 10, fill=CARD_BG)
        c.setFont("Helvetica", 10)
        c.setFillColor(TEXT_MID)
        c.drawCentredString(cx + card_w/2, card_y + card_h - 18, label)
        c.setFont("Helvetica-Bold", 18)
        c.setFillColor(TEXT_DARK)
        c.drawCentredString(cx + card_w/2, card_y + 12, value)

    full_w = W - 2*margin

    pie_label_y = card_y - 28
    c.setFont("Helvetica-Bold", 11)
    c.setFillColor(TEXT_DARK)
    c.drawString(margin, pie_label_y, "Subject Overview")

    pie_card_h = 160
    pie_card_y = pie_label_y - pie_card_h - 4
    _rrect(c, margin, pie_card_y, full_w, pie_card_h, 10, fill=WHITE)

    pie_r          = 56
    left_section_w = full_w * 0.40
    pie_cx         = margin + left_section_w / 2
    pie_cy         = pie_card_y + pie_card_h / 2

    total_tests = sum(s.get("tests", 1) for s in subjects) or 1
    slices = [
        {"pct": s.get("tests", 1) / total_tests,
         "color": SUBJECT_COLORS[i % len(SUBJECT_COLORS)],
         "avg": s.get("score", 0)}
        for i, s in enumerate(subjects)
    ]
    _pie(c, pie_cx, pie_cy, pie_r, slices)

    leg_x   = margin + left_section_w + 10
    leg_top = pie_card_y + pie_card_h - 20
    row_gap = min(20, (pie_card_h - 28) / max(len(subjects), 1))
    for i, s in enumerate(subjects):
        row_y = leg_top - i * row_gap
        if row_y < pie_card_y + 8:
            break
        c.setFillColor(SUBJECT_COLORS[i % len(SUBJECT_COLORS)])
        c.roundRect(leg_x, row_y, 10, 10, 2, fill=1, stroke=0)
        c.setFont("Helvetica", 8)
        c.setFillColor(TEXT_MID)
        c.drawString(leg_x + 15, row_y + 1, s.get("name", "")[:18].upper())
        c.setFont("Helvetica-Bold", 8)
        c.setFillColor(PURPLE)
        c.drawRightString(margin + full_w - 10, row_y + 1, f"{s.get('score',0)}%")

    trend_label_y = pie_card_y - 20
    c.setFont("Helvetica-Bold", 11)
    c.setFillColor(TEXT_DARK)
    c.drawString(margin, trend_label_y, "Score Trend")

    chart_h      = 160
    chart_card_y = trend_label_y - chart_h - 4

    trend_attempts = sorted(
        [a for a in attempts if a.get("attempt_date")],
        key=lambda a: str(a.get("attempt_date", ""))
    )
    if trend_attempts:
        trend_data, avg_data = [], []
        for a in trend_attempts:
            date_str = str(a.get("attempt_date", ""))[:10]
            try:
                dt    = datetime.strptime(date_str, "%Y-%m-%d")
                label = dt.strftime("%b %-d")
            except Exception:
                label = date_str[5:]
            trend_data.append({"label": label, "score": a.get("score", 0)})
            avg_data.append(a.get("class_avg", round(a.get("score", 0) * 0.9, 1)))
        _line_chart(c, margin, chart_card_y, full_w, chart_h, trend_data, avg_data)
    else:
        _rrect(c, margin, chart_card_y, full_w, chart_h, 10, fill=WHITE)
        c.setFont("Helvetica", 9)
        c.setFillColor(TEXT_LIGHT)
        c.drawCentredString(margin + full_w/2, chart_card_y + chart_h/2, "No attempt data")

    sc_label_y = chart_card_y - 24
    c.setFont("Helvetica-Bold", 11)
    c.setFillColor(TEXT_DARK)
    c.drawString(margin, sc_label_y, "Subject Scores")

    sc_y = sc_label_y - 68
    sc_h = 58
    n    = len(subjects)
    sc_w = min((W - 2*margin - max(n-1,0)*8) / max(n,1), 130) if n else 130
    for i, s in enumerate(subjects):
        sx = margin + i * (sc_w + 8)
        _rrect(c, sx, sc_y, sc_w, sc_h, 10, fill=CARD_BG)
        c.setFont("Helvetica", 8.5)
        c.setFillColor(TEXT_MID)
        c.drawCentredString(sx + sc_w/2, sc_y + sc_h - 15, s.get("name","")[:16])
        c.setFont("Helvetica-Bold", 17)
        c.setFillColor(TEXT_DARK)
        c.drawCentredString(sx + sc_w/2, sc_y + 10, f"{s.get('score',0)}%")

    _draw_shared_footer(c, W)
    c.save()
    buf.seek(0)
    return buf.read()


